#Как это работает для примера:
#n = 6 (количество чисел)
#Читаем 6 чисел: 3, 2, 42, 5, 12, 11
#Находим числа кратные 3:
#3 кратно 3 → sum = 3, count = 1
#2 не кратно 3
#42 кратно 3 → sum = 45, count = 2
#5 не кратно 3
#12 кратно 3 → sum = 57, count = 3
#11 не кратно 3
#Среднее арифметическое: 57 / 3 = 19.0
#Объяснение:
#Суммируем только числа, кратные 3
#Считаем количество таких чисел
#Если есть кратные числа - выводим среднее
#Если нет - выводим -1

n = int(input())
sum_numbers = 0
count = 0
for i in range(n):
    num = int(input())
    if num % 3 == 0:
        sum_numbers += num
        count += 1
if count > 0:
    average = sum_numbers / count
    print(average)
else:
    print(-1)
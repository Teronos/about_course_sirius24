'''
Вводится два целых числа a и b (a
b). Найдите самое большее число на отрезке от a до b, кратное 7. Если такого числа нет выведите -1.

Пример ввода:
100
500
Пример вывода:
497
'''
a = int(input())
b = int(input())

r = -1
for i in range(b, a - 1, -1):
    if i % 7 == 0:
        r = i
        break

print(r)
# По данному числу N распечатайте все целые значения степени двойки, не превосходящие N, в порядке возрастания.

number = int(input("Введите число: "))

if number == "":
    print("Ошибка ввода данных")
    exit(0)

if number <= 1:
    print("Таких целых чисел нет")
    exit(0)

i = 0
while 2**i < number:
    print(2**i, end = " ")
    i = i + 1